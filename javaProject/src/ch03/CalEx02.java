package ch03;

public class CalEx02 {
	public static void main(String[] args) {
		int a = 20;
		int b = 8;
		System.out.printf("%d\n", a+b);   //  \n은 행변환
		System.out.printf("a+b=%d\n", a+b);
		System.out.printf("a-b=%d\n", a-b);
		System.out.printf("a*b=%d\n", a*b);
		System.out.printf("a/b=%d\n", a/b);
		System.out.printf("a%%b=%d\n", a%b);  //%자체를 출력하기 위해서 2개를 써야됨.
		
	}
}
